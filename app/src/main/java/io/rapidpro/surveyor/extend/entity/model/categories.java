package io.rapidpro.surveyor.extend.entity.model;

public class categories {
    int count;
    String label;

    public categories() {
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
